package moduliertersingvogel.nexus.basics.webservice;

public class Resources {
	private int gruen=0;
	private int rot=0;
	private int orange=0;
	
	public Resources(){
		
	}
	
	public int getGruen() {
		return gruen;
	}
	public void setGruen(int gruen) {
		this.gruen = gruen;
	}
	public int getRot() {
		return rot;
	}
	public void setRot(int rot) {
		this.rot = rot;
	}
	public int getOrange() {
		return orange;
	}
	public void setOrange(int orange) {
		this.orange = orange;
	}
}
